package com.example.schedulo;

class CalendarEvent {
    private String name, description, location;
    private int start, end;

    public CalendarEvent (String name, String description, String location, int start, int end) {
        this.name = name;
        this. description = description;
        this.location = location;
        this.start = start;
        this.end = end;
    }
}
